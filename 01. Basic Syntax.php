<?php

    // Echo
    echo "Hello, World!";

    // Variable declaration
    $name = "Bob";
    echo "<p>Hello, $name</p>";

    // Conditional
    $age = 20;
    if ($age >= 18) {
        echo "<p>Adult</p>";
    } else {
        echo "<p>Child</p>";
    }

    // for loop
    echo "<ul>";
    for($i = 1; $i <= 5; $i++) {
        echo "<li>$i</li>";
    }
    echo "</ul>;"
?>
